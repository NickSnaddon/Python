##String Method
#prints the variable
Quote = "I am an excellent programmer"
print(Quote)

#prints the variable in lowercase
Quote = "I am an EXCELLENT programmer"
print(Quote.lower())

#prints the variable in uppercase
Quote = "i am an excellent programmer"
print(Quote.upper())

#capitalises the first letter of each word
Quote = "I am an excellent programmer"
print(Quote.title())

#swaps the cases of the characters
Quote = "I am an excellent programmer"
print(Quote.swapcase())

#Capitalises the first letter of the string
Quote = "i am an excellent programmer"
print(Quote.capitalize())
